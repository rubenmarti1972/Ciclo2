public class Universidad {
    
    private String nombre;
    private int nit;
    private String ciudad;

    public Universidad(String nombre, int nit, String ciudad){
        this.nombre = nombre;
        this.nit = nit;
        this.ciudad = ciudad;
    }

    public String getNombre(){
        return nombre;
    }
    public int getNit(){
        return nit;
    }

    public String getCiudad(){
        return ciudad;
    }

    public void setNombre(String nombre){
        this.nombre = nombre;
    }

    public void setCiudad(String ciudad){
        this.ciudad = ciudad;
    }

}class Universidad {
    
}
